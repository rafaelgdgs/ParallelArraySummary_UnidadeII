public class ParallelArrayObject {

    private int id;
    private int total;
    private int grupo;

    public void setId(int _id) {
        id = _id;
    }

    public int getId() {
        return id;
    }

    public void setTotal(int _total) {
        total = _total;
    }

    public int getTotal() {
        return total;
    }

    public void setGrupo(int _grupo) {
        grupo = _grupo;
    }

    public int getGrupo() {
        return grupo;
    }
}
